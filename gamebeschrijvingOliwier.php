<h1> Snake</h1>

<main>
    "Snake" is een iconisch computerspel dat voor het eerst werd geïntroduceerd in de late jaren '70. Het is een
    eenvoudig spel waarbij de speler een lijn bestuurt die groeit terwijl hij punten verzamelt, meestal voorgesteld als
    voedsel of een soortgelijk object. De "Snake" beweegt in een raster en de speler moet obstakels vermijden, waaronder
    de randen van het speelveld en de slang zelf.
    <br>
    Het doel van het spel is om zo lang mogelijk te overleven en een zo hoog mogelijke score te behalen. Naarmate de
    slang voedsel eet, wordt hij langer en sneller, wat de moeilijkheidsgraad verhoogt. Het spel eindigt wanneer de
    slang tegen een obstakel botst.
    <br>
    "Snake" heeft verschillende varianten en is verschenen op talloze platforms, van vroege homecomputers tot moderne
    smartphones. Het werd bijzonder populair in de late jaren '90 toen het werd voorgeïnstalleerd op Nokia mobiele
    telefoons.
    <br>
    Ondanks zijn eenvoud, of misschien wel dankzij, blijft "Snake" een geliefd spel. Het biedt een uitdagende en
    verslavende gameplay die gemakkelijk te leren is, maar moeilijk te beheersen. Het is een prachtig voorbeeld van hoe
    een eenvoudig concept kan leiden tot een boeiende ervaring. Het succes van "Snake" heeft de weg geëffend voor de
    mobiele gaming-industrie en blijft een klassieker in het genre. Het is een spel dat zowel nostalgie oproept als
    blijft boeien bij nieuwe generaties spelers.
